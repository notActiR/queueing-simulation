# ---- 问题 1: 马尔可夫性 ----
st.markdown("---")
st.markdown(r"## 问题 1: 为什么 $\{N_k\}$ 是马尔可夫链而 $\{X_t\}$ 不是?")

col1, col2 = st.columns(2)

with col1:
    st.success(r"""
    **$\{N_k\}$ 是马尔可夫链**

    $N_k$ 定义为第 $k$ 个任务**离开时**系统中的任务数。在离开时刻:
    - 正在服务的任务恰好完成, 离开系统
    - 下一个任务 (如果有) 即将开始服务
    - 此时不需要知道"剩余服务时间是多少"

    因此 $N_k$ 的演化只依赖 $N_{k-1}$ 和新到达的任务数, 满足马尔可夫性:
    $$P(N_k \mid N_{k-1}, N_{k-2}, \ldots) = P(N_k \mid N_{k-1})$$
    """)

with col2:
    st.warning(r"""
    **$\{X_t\}$ 一般不是马尔可夫过程**

    $X_t$ 定义为**任意时刻** $t$ 系统中的任务数。

    如果只知道 $X_t$, 我们不知道正在服务的任务"已经服务了多久", 从而无法确定它什么时候离开。

    对于 Erlang-2 服务, 剩余服务时间的分布取决于已消耗了多少, 因此 $X_t$ 的演化依赖于过去, 不满足马尔可夫性:

    $$P(X_{t+s} \mid X_u, u \leq t) \neq P(X_{t+s} \mid X_t)$$
    """)

st.markdown("---")
st.markdown("#### 四个过程之间的内在联系")

st.markdown(r"""
| 过程 | 定义时刻 | 状态空间 | 与其他过程的关系 |
|------|----------|----------|------------------|
| **$\{N_k\}$** | 离散离开时刻 | 离散 | 对 $X_t$ 在离开时刻的采样 |
| **$\{X_t\}$** | 连续时间 | 离散 | $N_k$ 是其嵌入链, $W_k$ 和 $Y_t$ 由其衍生 |
| **$\{W_k\}$** | 离散到达时刻 | 连续 | $W_k = $ (服务开始时间) - (到达时间), 反映排队延迟 |
| **$\{Y_t\}$** | 连续时间 | 连续 | $=$ 总需求 $-$ 已完成服务, 剩余部分影响 $W_k$ |
""")

# ---- 问题 2: 收敛性 ----
st.markdown("---")
st.markdown(r"## 问题 2: 收敛模式与 $\rho$ 的关系")

lam = st.session_state.get('lam', 0.7)
mu = st.session_state.get('mu', 1.0)
rho = lam / mu

st.markdown(rf"""
从第三章的收敛图中可以观察到:

1. **$E[N_k]$ 从 0 附近开始** (系统初始为空), 随 $k$ 增大单调上升, 逐渐趋于理论稳态值
2. **$\mathrm{{Var}}[N_k]$ 同样收敛**, 但收敛更慢, 且稳态方差通常大于稳态均值
3. **收敛速度与 $\rho$ 密切相关:**

| $\rho$ | $1/(1-\rho)$ | 收敛速度 | 现象 |
|---|---------|----------|------|
| 0.3 | 1.4 | 很快 | $k\approx 50$ 就接近稳态 |
| 0.5 | 2.0 | 较快 | $k\approx 100$ 基本稳定 |
| 0.7 | 3.3 | 中等 | $k\approx 200\sim 500$ 收敛 |
| 0.9 | 10.0 | 很慢 | $k>1000$ 仍有瞬态效应 |

这是因为系统的"记忆长度"大致与 $1/(1-\rho)$ 成正比:
$\rho$ 越大, 系统越难从繁忙状态恢复, 收敛越慢。
""")

# ---- 问题 3: 估计精度 ----
st.markdown("---")
st.markdown("## 问题 3: 统计推断的可靠性")

st.markdown(r"""
**30 个样本的估计精度:**

- **$E[N_k]$ 的估计**: 30 个独立样本给出的样本均值是 $E[N_k]$ 的无偏估计
  - 标准误 = $\sigma/\sqrt{30} \approx 0.18\sigma$, 相对精度约 18%
  - 对于大多数实用目的已足够

- **95% 置信区间**: 基于 $t$ 分布构造
  - 约有 95% 的概率真值落入区间内
  - 但要注意: 30 次仿真的 CI 宽度反映了**仿真估计**的不确定性, 不是 $N_k$ 本身的变异性

- **CI 的覆盖效果**:
  - 合理: 当理论值已知时, 可验证仿真均值是否落在 CI 内
  - 第三张的图表中, 蓝色 CI 带覆盖了红色理论线, 说明仿真与理论一致

- **方差估计**: 样本方差需要更多样本才能精确估计
  - 30 个样本对方差的估计精度约为 26% (相对误差)
  - 可以通过增加仿真次数来提高
""")

# ---- 问题 4: 应用启示 ----
st.markdown("---")
st.markdown("## 问题 4: 系统设计启示")

st.markdown(rf"""
从仿真结果中, 我们可以得出以下关于**计算服务器系统设计**的启示:

### 1. 负载率的选择 — 关键权衡

$\rho$ 的选择是在**服务器利用率**和**用户等待时间**之间的权衡:

- **$\rho = 0.5$**: 服务器一半时间空闲, 等待时间很短。适合对延迟敏感的实时系统
- **$\rho = 0.7$**: 业界常见折衷, 利用率 70%, 等待时间可接受。适合一般 Web 服务
- **$\rho = 0.85$**: 服务器很忙, 等待时间显著增长。适合批处理或对延迟不敏感的系统
- **$\rho \geq 0.9$**: 危险区域, 任何突发流量都可能导致队列爆炸

### 2. 变异性的影响

Erlang-2 ($C_s^2 = 0.5$) 的服务时间比指数分布 ($C_s^2 = 1.0$) 更"可预测":

- 更小的变异性 $\to$ 更短的期望等待时间
- **系统设计启示**: 标准化服务流程, 减少服务时间的波动, 可以有效降低排队延迟

### 3. 容量规划

理论公式 $E[X] = \rho + \frac{{0.75\rho^2}}{{1-\rho}}$ 可以在设计阶段估算:
- 给定到达率 $\lambda$ 和服务率 $\mu$, 预测平均队长和等待时间
- 确定满足 SLA (如 $P[W > 1s] < 5\%$) 需要多大的服务容量

### 4. 仿真 vs 理论

- 理论公式给出的是**稳态期望值**, 实际系统在有限时间内可能远未达到稳态
- 仿真可以捕捉瞬态行为、极端事件和分布形态, 提供更全面的系统理解
- 尤其在 $\rho$ 较高时, 瞬态效应不可忽略
""")

